#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	char	c;
	char	d;

	while (*s != '\0')
	{
		c = *s;
		write(fd, &c, 1);
		s++;
	}
	d = '\n';
	write(fd, &d, 1);
}
